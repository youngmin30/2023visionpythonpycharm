# 진법 변환 공부해야 하는 것

int_data = 10 # 10진수
bin_data = 0b10 # 0b 2진수 2의 1승 곱하기 1 + 2의 0승 곱하기 0
oct_data = 0o10 # 0o 8진수 8의 0승, 8의 1승
hex_data = 0x10 # 0x 16진수 8의 1승 곱하기 1 
long_data = 1234567890123456789

print(int_data)
print(bin_data)
print(oct_data)
print(hex_data)
print(long_data)


