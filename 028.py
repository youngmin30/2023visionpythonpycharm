strdata = "Time is money!!"

print(strdata[1:5]) # 1부터 5 앞까지를 자른다.
print(strdata[:7]) # 처음 0부터 7 앞까지를 자른다.
print(strdata[9:]) # 9부터 나머지를 자른다.
print(strdata[:-3]) # 처음 0부터 뒤에서 3까지를 자른다.
print(strdata[-3:]) # 뒤에서 -3부터 그 뒤로 끝까지 자른다.
print(strdata[:1]) # 처음 -부터 1까지 자른다.
print(strdata[::2]) # 처음부터 2까지 자른다. Tm smny!


